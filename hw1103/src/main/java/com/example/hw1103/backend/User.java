package com.example.hw1103.backend;

public class User {

    private String name;
    private String email;
    private int icon;

    public String getName() {
        return name;
    }

    public String getEmail() {
        return email;
    }

    public int getIcon() {
        return icon;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public void setIcon(int icon) {
        this.icon = icon;
    }
}
